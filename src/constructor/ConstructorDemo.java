package constructor;

public class ConstructorDemo {
    int a;
    String s;
    boolean b;


    public static void main(String[] args) {


        ConstructorDemo sd=new ConstructorDemo(); //constructor called automatically
        System.out.println("int a: "+ sd.a );
        System.out.println("String s: "+ sd.s );
        System.out.println("boolean b : "+ sd.b );



    }
}
